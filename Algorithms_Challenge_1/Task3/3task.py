import os
# запитуємо дані для х1, х2, у1, у2

x1 = int(input("Введіть значення х1: "))
y1 = int(input("Введіть значення у1: "))
x2 = int(input("Введіть значення х2: "))
y2 = int(input("Введіть значення у2: "))

# рахуємо D
D = x1 * y2 - x2 * y1

if D < 0:
    print("Відрізок OA утворює більший кут з віссю OX")
elif D > 0:
    print("Відрізок OB утворює більший кут з віссю OX")
else:
    print("Відрізки утворюють однаковий кут")

os.system("pause")